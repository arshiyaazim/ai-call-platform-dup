# ═══════════════════════════════════════════════════════════
# Conversation State — per-user intent tracking
# ═══════════════════════════════════════════════════════════

_CONV_STATE: dict[str, dict] = {}
_STATE_TTL = 300   # 5 minutes
_MAX_STATES = 500


def _get_state(conv_id: str) -> dict:
    s = _CONV_STATE.get(conv_id)
    if s and (_time.monotonic() - s.get("ts", 0)) < _STATE_TTL:
        return s
    return {}


def _set_state(conv_id: str, **kwargs):
    state = _get_state(conv_id)
    state.update(kwargs)
    state["ts"] = _time.monotonic()
    if len(_CONV_STATE) >= _MAX_STATES:
        oldest = min(_CONV_STATE, key=lambda k: _CONV_STATE[k].get("ts", 0))
        _CONV_STATE.pop(oldest, None)
    _CONV_STATE[conv_id] = state


def _clear_state(conv_id: str):
    _CONV_STATE.pop(conv_id, None)


