# Step 8: Smart Fallback — find 1-2 close topics and ask
# ═══════════════════════════════════════════════════════════

def _smart_fallback(norm: str, conv_id: str) -> str:
    """Generate contextual fallback with 1-2 suggested topics."""
    found: list[tuple[str, dict]] = []  # (suggestion_text, intent)
    seen_suggestions: set[str] = set()

    for intent in INTENTS:
        for word in intent.get("topics", []):
            if word in norm:
                suggestion = intent["suggestion"]
                if suggestion not in seen_suggestions:
                    seen_suggestions.add(suggestion)
                    found.append((suggestion, intent))
                break  # one match per intent is enough

    if len(found) == 1:
        suggestion_text, intent = found[0]
        # Store the suggested intent — if user confirms, we'll use it
        if intent.get("detail") and intent.get("followup"):
            reply_on_confirm = f"{intent['reply']}\n\n{intent['followup']}"
        else:
            reply_on_confirm = intent["reply"]
        _set_state(
            conv_id,
            suggested_intent=intent["name"],
            pending_detail=reply_on_confirm,
            pending_full_detail=intent.get("detail"),
        )
        return f"আপনি কি {suggestion_text} সম্পর্কে জানতে চাচ্ছেন?"

    elif len(found) >= 2:
        s1, _ = found[0]
        s2, _ = found[1]
        _set_state(conv_id, suggested_intent=None, pending_detail=None)
        return f"আপনি কি {s1} নাকি {s2} সম্পর্কে জানতে চাচ্ছেন?"

    # No partial match — generic fallback
    _set_state(conv_id, suggested_intent=None, pending_detail=None)
    return random.choice(_GENERIC_FALLBACKS)


