_THANKS_EN = {"thanks", "thank you", "thank"}

_FAREWELL_WORDS = ["আল্লাহ হাফেজ", "খোদা হাফেজ", "বিদায়", "ভালো থাকবেন"]
_FAREWELL_EN = {"bye", "goodbye", "see you"}

_STATEMENT_WORDS = ["ঠিক আছে", "বুঝেছি", "আচ্ছা", "ভালো"]
_STATEMENT_EN = {"okay", "ok", "understood", "fine", "good", "got it"}

_REQUEST_WORDS = ["লাগবে", "চাই", "দরকার", "প্রয়োজন", "দিন", "জানান", "পাঠান"]


def _classify(text: str, norm: str) -> str:
    """Classify message → greeting/thanks/farewell/negative/confirm/followup/statement/question/request/unknown."""
    low = text.lower().strip()
    words_en = set(low.split())

    # Greeting
    if any(g in norm for g in _GREETING_WORDS) or words_en & _GREETING_EN:
        return "greeting"

    # Thanks
    if any(t in norm for t in _THANKS_WORDS) or words_en & _THANKS_EN:
        return "thanks"

    # Farewell
    if any(f in norm for f in _FAREWELL_WORDS) or words_en & _FAREWELL_EN:
        return "farewell"

    # Negative / dismissive (check BEFORE confirm — "পরে জানাবো" > "জানাবো")
    if any(n in norm for n in _NEGATIVE_WORDS) or words_en & _NEGATIVE_EN:
        return "negative"

    # Confirmation (short messages only — max 5 words)
    if len(low.split()) <= 5:
        if any(c in norm for c in _CONFIRM_WORDS) or words_en & _CONFIRM_EN:
            return "confirm"

    # Follow-up detection (short messages that extend previous context)
    if len(low.split()) <= 6:
        if any(f in norm for f in _FOLLOWUP_WORDS_BN) or any(f in low for f in _FOLLOWUP_EN):
            return "followup"

    # Statement / acknowledgment
    if any(s in norm for s in _STATEMENT_WORDS) or words_en & _STATEMENT_EN:
        return "statement"

    # Question marks
    if any(qm in text for qm in _Q_MARKS):
        return "question"

    # Bangla question words
    for qw in _Q_WORDS_BN:
        if qw in norm:
            return "question"

    # English question words
    if words_en & _Q_WORDS_EN:
        return "question"

    # Request pattern (implicit question)
    if any(w in norm for w in _REQUEST_WORDS):
        return "request"

    return "unknown"


